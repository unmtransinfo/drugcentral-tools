SELECT
	s.ID,
	SUBSTR(s.NAME,1,80) "STRUCT_NAME",
	syn.NAME "SYNONYM"
FROM
	APP.STRUCTURES AS s,
	APP.SYNONYMS AS syn
WHERE
	syn.ID = s.ID
	AND UPPER(syn.NAME) LIKE '%ASPIRIN%'
	;
