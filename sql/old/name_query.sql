SELECT DISTINCT
	ai.STRUCT_ID,
	ai.ACTIVE_MOIETY_NAME,
	ai.SUBSTANCE_NAME
FROM
	APP.ACTIVE_INGREDIENT AS ai
WHERE
	UPPER(ai.SUBSTANCE_NAME) LIKE '%ASPIRIN%'
ORDER BY
	LENGTH(ai.ACTIVE_MOIETY_NAME),
	ai.STRUCT_ID
	;
--
SELECT DISTINCT
	ai.STRUCT_ID,
	ai.ACTIVE_MOIETY_NAME,
	ai.SUBSTANCE_NAME,
	s.CAS_REG_NO,
	s.CD_SMILES
FROM
	APP.ACTIVE_INGREDIENT AS ai,
	APP.STRUCTURES AS s
WHERE
	ai.STRUCT_ID = s.ID
	AND UPPER(ai.SUBSTANCE_NAME) LIKE '%ASPIRIN%'
ORDER BY
	LENGTH(s.CD_SMILES),
	ai.STRUCT_ID
	;
